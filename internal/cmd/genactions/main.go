func main() {
	if len(os.Args) != 4 {
		fmt.Fprintf(os.Stderr, "usage: %s <package-name> <source.go> <output.go>\n", filepath.Base(os.Args[0]))
		os.Exit(1)
	}

	pkgName := os.Args[1]
	sourcePath := os.Args[2]
	outputPath := os.Args[3]

	actions, err := collectActionConstants(sourcePath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "cannot collect actions: %v\n", err)
		os.Exit(1)
	}

	var b strings.Builder
	b.WriteString(iscHeader)
	b.WriteString("// Code generated by internal/cmd/genactions; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkgName)
	b.WriteString("// DeclaredActions returns every IAM action constant declared in this package.\n")
	b.WriteString("func DeclaredActions() []string {\n")
	b.WriteString("\treturn []string{\n")

	for _, action := range actions {
		fmt.Fprintf(&b, "\t\t%s,\n", action.name)
	}

	b.WriteString("\t}\n")
	b.WriteString("}\n")

	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		fmt.Fprintf(os.Stderr, "cannot format output: %v\n", err)
		os.Exit(1)
	}

	if err := os.WriteFile(outputPath, formatted, 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "cannot write output: %v\n", err)
		os.Exit(1)
	}
}

type actionConst struct {
	name  string
	value string
}

func collectActionConstants(path string) ([]actionConst, error) {
	fset := token.NewFileSet()

	f, err := parser.ParseFile(fset, path, nil, 0)
	if err != nil {
		return nil, err
	}

	actions := make([]actionConst, 0)

	for _, decl := range f.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || genDecl.Tok != token.CONST {
			continue
		}

		for _, spec := range genDecl.Specs {
			valueSpec, ok := spec.(*ast.ValueSpec)
			if !ok {
				continue
			}

			for i, name := range valueSpec.Names {
				if !strings.HasPrefix(name.Name, "Action") {
					continue
				}

				if i >= len(valueSpec.Values) {
					continue
				}

				lit, ok := valueSpec.Values[i].(*ast.BasicLit)
				if !ok || lit.Kind != token.STRING {
					continue
				}

				value, err := strconvUnquote(lit.Value)
				if err != nil {
					return nil, err
				}

				if !strings.Contains(value, ":") {
					continue
				}

				actions = append(actions, actionConst{name: name.Name, value: value})
			}
		}
	}

	sort.Slice(actions, func(i, j int) bool {
		return actions[i].value < actions[j].value
	})

	return actions, nil
}

func strconvUnquote(s string) (string, error) {
	if len(s) < 2 {
		return "", fmt.Errorf("invalid string literal: %s", s)
	}

	return s[1 : len(s)-1], nil
}
